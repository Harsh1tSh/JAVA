public class Swithc {
    public static void main(String[] args){
        // switch 
        int month = 7;
        switch (month){
            case 1 :
                System.out.println("This is first case");
                break;
            
            case 2 :
                System.out.println("This is Second case");
                break;

            case 3 :
                System.out.println("This is third case");
                break;

            default :
                System.out.println("Out of range");
        }
    }
}
