public class data_type {
    public static void main(String[] args){
        int newTemp = 30; // 4 bytes
        float newFloat = 10.4f; // 4 bytes
        boolean newBool = true; // 1 byte
        char newChar = 'E'; // 2 bytes
        String newString = "New Message"; 

        byte neVal = 120;
        short newShort = 3000; // whole numbers
        long newLong = 3000000000000L; // 2 byte
        double newDouble = 34.5d; 

        System.out.println(newTemp);
        System.out.println(newFloat);
        System.out.println(newBool);
        System.out.println(newString);
        System.out.println(neVal);
        System.out.println(newShort);
        System.out.println(newLong);
        System.out.println(newDouble);
        System.out.println(newChar);


    }
}
