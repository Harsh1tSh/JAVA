public class DataTypesReal {
    public static void main(String[] args){
        float myFloat;
        myFloat = 50.45f;
        System.out.println("The value of myFloat is : " + myFloat);

        myFloat = (float)50/3;
        System.out.println("Value after division of myFloat" + myFloat);

        double myDouble;
        myDouble = -0.2356;
        System.out.println("The value of Mydouble : " + myDouble);

        myDouble = 22d / 7;
        System.out.println("The valueof My Double is : " + myDouble);
    }
}
