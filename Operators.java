public class Operators {
    public static void main(String[] args){
        int val1 = 30;
        int val2 = 40;

        System.out.println(val1 + val2);
        System.out.println(val1 - val2);
        System.out.println(val1 * val2);
        System.out.println(val1 / val2);
        System.out.println(val1 % val2);
        System.out.println(val1++);
        System.out.println(++val2);
    }
}
